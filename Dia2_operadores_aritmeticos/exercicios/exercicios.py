def soma_de_valores(a,b):
    soma = a + b
    return soma 

def subtracao_de_valores(a,b):
    sub = a - b
    return sub

def multi(a,b):
    multi = a * b
    return multi

def divisao(a,b):
    div = a / b
    return div

def divisao_por_inteiro(a,b):
    div = a // b
    return div 

def resto_da_divisao(a,b):
    rest_div = a % b
    return rest_div

def potenciacao(a,b):
    pot = a ** b
    return pot